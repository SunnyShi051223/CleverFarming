/**
 * 根据城市名称获取城市编码
 * @param {string} cityName - 城市名称
 * @returns {string} 城市编码，如果找不到返回默认值
 */
function getCityCode(cityName) {
    // 移除"市"后缀再查找
    const nameWithoutSuffix = cityName.replace('市', '');
    
    // 先尝试完整匹配
    if (CITY_CODES[cityName]) {
        return CITY_CODES[cityName];
    }
    
    // 再尝试不带"市"的匹配
    if (CITY_CODES[nameWithoutSuffix]) {
        return CITY_CODES[nameWithoutSuffix];
    }
    
    // 默认返回武汉
    console.warn(`未找到城市 "${cityName}" 的编码，使用默认值（武汉）`);
    return '420100';
}

/**
 * 根据城市编码获取城市名称
 * @param {string} adcode - 城市编码
 * @returns {string} 城市名称
 */
function getCityName(adcode) {
    for (const [name, code] of Object.entries(CITY_CODES)) {
        if (code === adcode && !name.includes('市')) {
            return name;
        }
    }
    return '未知城市';
}

/**
 * 获取所有支持的城市列表
 * @returns {Array} 城市列表
 */
function getSupportedCities() {
    const cities = [];
    const seen = new Set();
    
    for (const [name, code] of Object.entries(CITY_CODES)) {
        if (!seen.has(code) && !name.includes('市')) {
            cities.push({ name, code });
            seen.add(code);
        }
    }
    
    return cities.sort((a, b) => a.name.localeCompare(b.name, 'zh-CN'));
}

